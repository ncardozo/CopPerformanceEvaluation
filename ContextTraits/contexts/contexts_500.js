function testContexts() {
    var i;
    var j;
    var ITER = 1000;
    var CALLS = 100;
    var beginning_time = new Date().getTime();
    for(i = 0; i < ITER; i++) {
        num = 1 + Math.floor(Math.random()*500);
        eval("Context"+num+".activate();");
        for(j=1; j<=CALLS; j++) {
          foo.test();
        }
        eval("Context"+num+".deactivate();");
    }
    var end_time = new Date().getTime();
    return end_time - beginning_time;
} 

